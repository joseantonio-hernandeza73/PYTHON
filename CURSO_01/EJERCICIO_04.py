# Operadores aritmeticos.

x = 2
y = 3

print("************SUMA******************")
print("Este es el resultado de luma de x + y = : ", x + y)
print("")
print("************RESTA******************")
print("Este es el resultado de Resta de x - y = : ", x - y)
print("")
print("********MULTIPLICACION*************")
print("Este es el resultado de Multiplicación de x * y = : ", x * y)
print("")
print("************DIVISON****************")
print("Este es el resultado de la División de x // y = : ", float(x // y))
print("")
print("************Exponente****************")
print("Este es el resultado de el exponente de x ** y = : ", x ** y)
print("")


